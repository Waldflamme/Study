x1,y1 = map(int,input('Enter first point: ').split())
x2,y2 = map(int,input('Enter second point: ').split())
x3,y3 = map(int,input('Enter first point: ').split())
side1 = float(((x2 - x1)**2+(y2 - y1)**2)**0.5)
side2 = float(((x3 - x2)**2+(y3 - y2)**2)**0.5)
side3 = float(((x3 - x1)**2+(y3 - y1)**2)**0.5)
print('Perimeter is: ', side1 + side2 + side3)
p = float(side1+side2+side3/2)
print('Area is: ', (p*(p-side1)*(p-side2)*(p-side3))**0.5)